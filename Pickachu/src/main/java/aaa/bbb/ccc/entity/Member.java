package aaa.bbb.ccc.entity;

import java.util.Date;

public class Member {

	private Integer idMember;
	
	private String eMailMember;
	
	private String passwordMember;
	
	private String penName;
	
	private String roleMember;
	
	private Date createdMember;
	
	private Date modifiedMember;

	
	
	
	
	public Integer getIdMember() {
		return idMember;
	}

	public void setIdMember(Integer idMember) {
		this.idMember = idMember;
	}

	public String geteMailMember() {
		return eMailMember;
	}

	public void seteMailMember(String eMailMember) {
		this.eMailMember = eMailMember;
	}

	public String getPasswordMember() {
		return passwordMember;
	}

	public void setPasswordMember(String passwordMember) {
		this.passwordMember = passwordMember;
	}

	public String getPenName() {
		return penName;
	}

	public void setPenName(String penName) {
		this.penName = penName;
	}

	public String getRoleMember() {
		return roleMember;
	}

	public void setRoleMember(String roleMember) {
		this.roleMember = roleMember;
	}

	public Date getCreatedMember() {
		return createdMember;
	}

	public void setCreatedMember(Date createdMember) {
		this.createdMember = createdMember;
	}

	public Date getModifiedMember() {
		return modifiedMember;
	}

	public void setModifiedMember(Date modifiedMember) {
		this.modifiedMember = modifiedMember;
	}
	

	
	
	
}
